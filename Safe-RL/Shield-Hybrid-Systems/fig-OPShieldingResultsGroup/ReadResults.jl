function experiment_order(a)
	order = [
		"Layabout", 
		"PreShielded", 
		"NoShield", 
		"PostShielded", 
		"PostShieldedRandomChoice", 
		"PostShieldedPolicyPreferred", 
		"PostShieldedInterventionMinimized", 
		"PostShieldedCostMinimized"
	]
	result = findall(==(a), order)

	if length(result) != 1
		throw(error("Undefined experiment name: $a"))
	end
	
	return result[1]
end

