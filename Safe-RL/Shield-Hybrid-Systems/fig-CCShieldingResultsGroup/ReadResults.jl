percentage_difference(a, b) = ((b - a)/a * 100)

# ╔═╡ 931094b3-2be4-4541-a374-ae137ba300c4
function formatted_percentage_difference(a, b)
	result = percentage_difference(a, b)
	if abs(result) < 0.1
		result < 0 ? "< -0.1%" : "< 0.1%"
	else
		@sprintf("%.1f", result)*"\\%"
	end
end

